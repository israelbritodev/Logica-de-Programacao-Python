preco = 10 # um valor inteiro
print(preco)
# saída: 10

preco = float(preco) # o valor esta sendo convertido de int para float através do construtor
print(preco)
# saida: 10.0

preco = 10.30
print(preco)
#saida: 10.3 valor como float

preco = int(preco)
print(preco)
#saida: 10 valor como inteiro pois foi convertido

